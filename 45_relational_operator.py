# relatinal operators

num1 = int(input("enter first number"))
num2 = int(input("enter second number"))

result = num1 == num2 #false 10 == 20
#false = 10 == 20
#print(result,"=",num1,"==",num2)
print(f"{result} = {num1} == {num2}")

result = num1 != num2 #true
print(f"{result} ={num1} != {num2}")

result = num1 < num2
print(f"{result} ={num1} < {num2}")

result = num1 > num2
print(f"{result} = {num1} > {num2}")

result = num1 <= num2
print(f"{result} = {num1} <= {num2}")

result = num1 >= num2
print(f"{result} = {num1} >= {num2}")